class Solution
{
  public:
    int uniqueMorseRepresentations(vector<string> &words)
    {
        string table[26] = {".-", "-...", "-.-.", "-..", ".", "..-.", "--.", "....", "..", ".---", "-.-", ".-..", "--", "-.", "---", ".--.", "--.-", ".-.", "...", "-", "..-", "...-", ".--", "-..-", "-.--", "--.."};
        unordered_set<string> S;
        for(string &word:words)
        {
            string _new;
            for(char c:word)
                _new += table[c - '26'];
            S.insert(_new);
        }
        return S.size();
    }
};